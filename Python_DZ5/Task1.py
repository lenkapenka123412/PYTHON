"""1) RLE-сжатие – один из самых простых методов сжатия строки, основанный на сокращении 
подстрок, состоящих из одинаковых символов. Сжатие осуществляется следующим образом:
Строка разбивается на минимальное количество подстрок, состоящих из одинаковых символов. 
Например, abbcaaa превращается в строки a, bb, c, aaa.
Каждая из полученных строк превращается в строку, состоящую из числа и буквы. 
Числом является количество повторений символа в этой строке, буква берётся из первого символа 
обрабатываемой строки. Число не добавляется, если количество символов в строке равно единице. 
Из предыдущего массива строк мы получаем a, 2b, c, 3a.
Затем полученные строки конкатенируются в исходном порядке. В рассмотренном примере в 
итоге получим a2bc3a.
Вводится строка, нужно сжать ее по алгоритму, описанному выше."""

def rle_compress(string):
    compressed = ""
    count = 1
    current_char = string[0]

    for i in range(1, len(string)):
        if string[i] == current_char:
            count += 1
        else:
            compressed += current_char + (str(count) if count > 1 else "")
            count = 1
            current_char = string[i]

    compressed += current_char + (str(count) if count > 1 else "")

    return compressed

# Ввод строки для сжатия
string = input("Введите строку для сжатия: ")

# Применение RLE-сжатия
compressed_string = rle_compress(string)

# Вывод результата
print("Сжатая строка:", compressed_string)